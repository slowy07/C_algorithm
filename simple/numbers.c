#include <stdio.h>
#include <stdlib.h>


int main()
{
    
    printf("float number : %f", 42.33);
    printf("\n");
    printf("decimal number: %d", 4 + 4);
    printf("\n");
    printf("number with pow() : %f \n", pow(3,2));
    printf("number with ceil() : %f \n", ceil(46.72));
    printf("number with floor() : %f", floor(36.25));

    return 0;
}